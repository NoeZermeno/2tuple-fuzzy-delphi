function linguisticLabel($index , $criteria){
	$item = getItem($index);
	

	if($criteria != 'Total' && $criteria != 'level' ){
        $integer = round($item[$criteria]);
		return  "(s<sub>" . $integer  . "</sub> , " .round($item[$criteria] - $integer,2) . ")";
	}

	if ($criteria == 'Total'){
        $integer = round($item['score']);
		return  "( " . $item[$criteria]  . "</sub> , " .($item['score'] - $integer) . ")";
	}
    
    if ($criteria == 'level'){
        return round($item['score']+1);
    }


}

